	DOSSEG
	.MODEL SMALL
	.STACK 32
	.DATA

NUMERE  DB  0Dh,0Ah,'mat(1,1)=$'
M DB 4
N DB 4
	.CODE
START:	MOV AX,@DATA
	MOV DS,AX
	;
    XOR CX,CX
	MOV CL,[M]; nr linii

COLOANE:
    PUSH CX
    XOR CX,CX
    MOV CL,[N] ; nr cols
LINIE:	
    MOV DX,OFFSET NUMERE
	MOV AH,9
	INT 21H
	INC [NUMERE+8]
	LOOP LINIE
    INC [NUMERE+6]
    MOV [NUMERE+8],31h
    POP CX
    LOOP COLOANE
	;
	MOV AH,4CH
	INT 21H
	END START
